<?php

$x = 10;  
$a = ++$x;
echo "pre increment : $a \n"; //Increments $x by one, then returns $x

$b = $x++;
echo "post increment : $b \n"; //Returns $x, then increments $x by one

$c = --$x;
echo "pre decrement :$c \n"; //Decrements $x by one, then returns $x

$d = $x--;
echo "post decrement : $d \n"; //Returns $x , then decrements $x by one 



?>  